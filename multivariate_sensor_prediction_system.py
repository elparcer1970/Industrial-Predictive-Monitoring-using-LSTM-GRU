# ── Asymmetric RUL loss (NASA PHM convention) ─────────────────────────────
def rul_asymmetric_loss(y_true, y_pred):
    err = y_true - y_pred
    return tf.reduce_mean(
        tf.where(err >= 0,
                 0.5 * tf.square(err),    # under-estimate
                 1.5 * tf.square(err))    # over-estimate penalised more
    )

